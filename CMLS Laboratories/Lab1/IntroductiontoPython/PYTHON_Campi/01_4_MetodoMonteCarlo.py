import random

PUNTI=100
punti=0 
for p in range(PUNTI):
    x=random.random()
    y=random.random()
    if(x**2+y**2 <= 1): 
        punti+=1
pigreco=4*punti/PUNTI
print("Pigreco approsimato con ",PUNTI," punti: ",pigreco) 


